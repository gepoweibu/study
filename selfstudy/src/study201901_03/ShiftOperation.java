package study201901_03;

public class ShiftOperation {

	public static void main(String[] args) {
		System.out.println("1<<1: "+Integer.toBinaryString((1<<1)));
		System.out.println("1<<2: "+Integer.toBinaryString((1<<2)));
		System.out.println("1>>1: "+Integer.toBinaryString((1>>1)));
		System.out.println("2<<1: "+Integer.toBinaryString((2<<1)));
		System.out.println("2<<2: "+Integer.toBinaryString((2<<2)));
	}

}
